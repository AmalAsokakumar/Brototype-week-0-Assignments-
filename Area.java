public class Area {
     public  double circle(int radius){
     
      return ( 3.14159* radius *radius);
          
     }
     public  double square(int side){

          return side*side;

     }
     public float rectangle(int length, int width){

          return length*width;

     }
     public float triangle(int base, int height){

          return (base*height)/2;

     }
     
}
